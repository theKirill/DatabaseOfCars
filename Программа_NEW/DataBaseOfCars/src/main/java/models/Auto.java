package models;

public class Auto {
    Integer id;
    String fullNameAuto;
    String city;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFullNameAuto() {
        return fullNameAuto;
    }

    public void setFullNameAuto(String fullNameAuto) {
        this.fullNameAuto = fullNameAuto;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
